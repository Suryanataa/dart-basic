void main() {
  var a;

  // mengisi nilai bertipe int ke dalam variable a
  a = 10;
  print("\nnilai a:\t" + a.toString());
  print("tipe  a:\t" + a.runtimeType.toString());

  // mengisi nilai bertipe double ke dalam variable a
  a = 10.21;
  print("\nnilai a:\t" + a.toString());
  print("tipe  a:\t" + a.runtimeType.toString());

  // mengisi nilai bertipe bool ke dalam variable a
  a = true;
  print("\nnilai a:\t" + a.toString());
  print("tipe  a:\t" + a.runtimeType.toString());

  // mengisi nilai bertipe string ke dalam variable a
  a = "10";
  print("\nnilai a:\t" + a.toString());
  print("tipe  a:\t" + a.runtimeType.toString());
}
